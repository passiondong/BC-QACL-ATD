def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not rows:
        path.write_text("", encoding="utf-8")
        return
    fields: list[str] = []
    for row in rows:
        for key, value in row.items():
            if key not in fields and not isinstance(value, (dict, list)):
                fields.append(key)
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def rank_rows(rows: list[dict[str, Any]], prefix: str) -> list[dict[str, Any]]:
    out = sorted(
        rows,
        key=lambda row: (
            row["omn_z_loss"],
            row["s21_ripple_35_75_db"],
            abs(row["s21_mean_35_75_db"] - 18.0),
            row["W_IMN_um"],
            row["R_IMN"],
            row["W_ISMN_um"],
            row["R_ISMN"],
            row["W_OMN_um"],
            row["R_OMN"],
        ),
    )
    lower_by_loss = {}
    losses = sorted({round(row["omn_z_loss"], 15) for row in out})
    for loss in losses:
        lower_by_loss[loss] = 1 + sum(1 for row in out if round(row["omn_z_loss"], 15) < loss)
    for idx, row in enumerate(out, start=1):
        row[f"{prefix}_row_rank"] = idx
        row[f"{prefix}_competition_rank_by_omn_z"] = lower_by_loss[round(row["omn_z_loss"], 15)]
    return out


def unique_omn_rank(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    by_omn: dict[tuple[float, float], dict[str, Any]] = {}
    for row in rows:
        key = wr_key(row["W_OMN_um"], row["R_OMN"])
        by_omn.setdefault(
            key,
            {
                "W_OMN_um": row["W_OMN_um"],
                "R_OMN": row["R_OMN"],
                "omn_z_loss": row["omn_z_loss"],
                "omn_mean_abs_z_error_ohm": row["omn_mean_abs_z_error_ohm"],
                "omn_max_abs_z_error_ohm": row["omn_max_abs_z_error_ohm"],
                "qualified_combo_count_with_this_omn": 0,
            },
        )
        by_omn[key]["qualified_combo_count_with_this_omn"] += 1
    vals = sorted(by_omn.values(), key=lambda row: (row["omn_z_loss"], row["W_OMN_um"], row["R_OMN"]))
    for idx, row in enumerate(vals, start=1):
        row["unique_omn_rank"] = idx
    return vals


