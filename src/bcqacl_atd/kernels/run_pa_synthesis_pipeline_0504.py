def plot_omn_z(loadpull, omn_scores: dict[tuple[float, float], dict[str, Any]], cases: list[tuple[str, float, float]], path: Path) -> None:
    fig, axes = plt.subplots(2, 1, figsize=(8.5, 6.0), dpi=160, sharex=True)
    f = loadpull.freq_ghz
    axes[0].plot(f, loadpull.zopt_single.real, "o-", linewidth=1.8, label="Loadpull Zopt Re")
    axes[1].plot(f, loadpull.zopt_single.imag, "o-", linewidth=1.8, label="Loadpull Zopt Im")
    for label, w, r in cases:
        score = omn_scores[wr_key(w, r)]
        axes[0].plot(f, score["zavg_re_ohm"], "s--", linewidth=1.4, label=f"{label} Re W{w:g} R{r:g}")
        axes[1].plot(f, score["zavg_im_ohm"], "s--", linewidth=1.4, label=f"{label} Im W{w:g} R{r:g}")
    axes[0].set_ylabel("Resistance (ohm)")
    axes[1].set_ylabel("Reactance (ohm)")
    axes[1].set_xlabel("Frequency (GHz)")
    for ax in axes:
        ax.grid(True, alpha=0.3)
        ax.legend(loc="best")
    fig.suptitle("OMN Zin vs Loadpull Zopt After PA Qualification")
    fig.tight_layout()
    fig.savefig(path)
    plt.close(fig)


def main() -> int:
    t0 = time.perf_counter()
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    FIG_DIR.mkdir(parents=True, exist_ok=True)
    CACHE_DIR.mkdir(parents=True, exist_ok=True)

    cfg = PaSynthesisConfig()
    loadpull = load_loadpull_zopt(cfg.paths.loadpull_excel, f_start_ghz=30.0, f_stop_ghz=80.0)
    loadpull_indices = [int(np.argmin(np.abs(FREQ_GHZ - fghz))) for fghz in loadpull.freq_ghz]

    driver = load_transistor_s4p(cfg.paths.driver_transistor_s4p, target_freq_hz=FREQ_HZ, z0=50.0, name="driver_2x12")
    final = load_transistor_s4p(cfg.paths.final_transistor_s4p, target_freq_hz=FREQ_HZ, z0=50.0, name="final_2x18")
    predictor = Transformer0504Predictor()

    imn_grid, ismn_grid, omn_grid = candidate_grids()
    all_wr = sorted({wr_key(w, r) for w, r in imn_grid + ismn_grid + omn_grid})
    tf_cache: dict[tuple[float, float], rf.Network] = {}
    for idx, (w, r) in enumerate(all_wr, start=1):
        tf_cache[(w, r)] = predictor.build(w, r, name=f"tf6_0504_W{w:g}_R{r:g}")
        if idx % 25 == 0:
            print(f"[build] {idx}/{len(all_wr)} transformer networks")

    omn_scores: dict[tuple[float, float], dict[str, Any]] = {}
    for w, r in omn_grid:
        key = wr_key(w, r)
        z1, z2, zavg_full = transformer_omn_input_impedances(tf_cache[key], load_ohm=50.0)
        zavg = zavg_full[loadpull_indices]
        err = zavg - loadpull.zopt_single
        loss = omn_impedance_loss(zavg, loadpull.zopt_single, cfg.objectives)
        omn_scores[key] = {
            "omn_z_loss": float(loss.loss),
            "omn_mean_abs_z_error_ohm": float(np.mean(np.abs(err))),
            "omn_median_abs_z_error_ohm": float(np.median(np.abs(err))),
            "omn_rms_abs_z_error_ohm": float(np.sqrt(np.mean(np.abs(err) ** 2))),
            "omn_max_abs_z_error_ohm": float(np.max(np.abs(err))),
            "omn_mean_real_error_ohm": float(np.mean(err.real)),
            "omn_mean_imag_error_ohm": float(np.mean(err.imag)),
            "zavg_re_ohm": zavg.real.tolist(),
            "zavg_im_ohm": zavg.imag.tolist(),
        }

    rows: list[dict[str, Any]] = []
    response_by_key: dict[tuple[float, float, float, float, float, float], dict[str, Any]] = {}
    total = len(imn_grid) * len(ismn_grid) * len(omn_grid)
    evaluated = 0
    last_print = time.perf_counter()
    for w_imn, r_imn in imn_grid:
        for w_ismn, r_ismn in ismn_grid:
            for w_omn, r_omn in omn_grid:
                combo_key = (w_imn, r_imn, w_ismn, r_ismn, w_omn, r_omn)
                try:
                    y2 = build_full_pa_y_fast(
                        driver_s4p=driver,
                        final_s4p=final,
                        imn=tf_cache[wr_key(w_imn, r_imn)],
                        ismn=tf_cache[wr_key(w_ismn, r_ismn)],
                        omn=tf_cache[wr_key(w_omn, r_omn)],
                    )
                    gain, s11, metrics = gain_metrics_from_y(y2, z0=50.0)
                    row = {
                        "status": "ok",
                        "W_IMN_um": float(w_imn),
                        "R_IMN": float(r_imn),
                        "W_ISMN_um": float(w_ismn),
                        "R_ISMN": float(r_ismn),
                        "W_OMN_um": float(w_omn),
                        "R_OMN": float(r_omn),
                        **metrics,
                        **{k: v for k, v in omn_scores[wr_key(w_omn, r_omn)].items() if not isinstance(v, list)},
                        "is_reference_group": bool(
                            wr_key(w_imn, r_imn) == wr_key(REFERENCE["IMN"]["W_um"], REFERENCE["IMN"]["R"])
                            and wr_key(w_ismn, r_ismn) == wr_key(REFERENCE["ISMN"]["W_um"], REFERENCE["ISMN"]["R"])
                            and wr_key(w_omn, r_omn) == wr_key(REFERENCE["OMN"]["W_um"], REFERENCE["OMN"]["R"])
                        ),
                    }
                    rows.append(row)
                    if row["is_reference_group"]:
                        response_by_key[combo_key] = {"s21_db": gain.tolist(), "s11_db": s11.tolist()}
                except Exception as exc:
                    rows.append(
                        {
                            "status": "error",
                            "error": str(exc),
                            "W_IMN_um": float(w_imn),
                            "R_IMN": float(r_imn),
                            "W_ISMN_um": float(w_ismn),
                            "R_ISMN": float(r_ismn),
                            "W_OMN_um": float(w_omn),
                            "R_OMN": float(r_omn),
                        }
                    )
                evaluated += 1
                if evaluated % 1000 == 0 or time.perf_counter() - last_print > 60.0:
                    print(f"[eval] {evaluated}/{total} full PA candidates")
                    last_print = time.perf_counter()

    ok_rows = [row for row in rows if row.get("status") == "ok"]
    strict_rows = [row for row in ok_rows if row["strict_pass_35_75"]]
    ideal_rows = [row for row in ok_rows if row["ideal_pass_30_80"]]
    strict_ranked = rank_rows(strict_rows, "strict35_75")
