def safe_tag(value: float, digits: int = 5) -> str:
    text = f"{float(value):.{digits}f}".rstrip("0").rstrip(".")
    return text.replace("-", "m").replace(".", "p")


def wr_key(w_um: float, r: float) -> tuple[float, float]:
    return round(float(w_um), 6), round(float(r), 6)


def candidate_grids() -> tuple[list[tuple[float, float]], list[tuple[float, float]], list[tuple[float, float]]]:
    imn = [
        (w, r)
        for w in inclusive_float_range(108.0, 114.0, 1.0)
        for r in [1.35, 1.40, 1.45, 1.50, 1.55, 1.60]
    ]
    ismn = [
        (w, r)
        for w in inclusive_float_range(104.0, 107.0, 0.5)
        for r in [1.20, 1.22, 1.24, 1.26, 1.28, 1.30]
    ]
    omn = [
        (w, r)
        for w in inclusive_float_range(100.0, 104.0, 0.5)
        for r in [1.60, 1.62, 1.64, 1.66, 1.68, 1.70, 1.72, 1.74, 1.75]
    ]
    for role, grid in [
        ("IMN", imn),
        ("ISMN", ismn),
        ("OMN", omn),
    ]:
        ref = wr_key(REFERENCE[role]["W_um"], REFERENCE[role]["R"])
        if ref not in {wr_key(w, r) for w, r in grid}:
            grid.append(ref)
    return imn, ismn, omn


class Transformer0504Predictor:
    def __init__(self) -> None:
        self.cache: dict[tuple[float, float], rf.Network] = {}
        S4P_DIR.mkdir(parents=True, exist_ok=True)
        S6P_DIR.mkdir(parents=True, exist_ok=True)

    def s4p_path_for(self, w_um: float, r: float) -> Path:
        return S4P_DIR / Cal_0504.default_filename(w_um, r, Cal_0504.DEFAULT_LENGTH_SCALE, Cal_0504.DEFAULT_SLOT_RATIO)

    def build(self, w_um: float, r: float, *, name: str) -> rf.Network:
        key = wr_key(w_um, r)
        if key in self.cache:
            out = self.cache[key].copy()
            out.name = name
            return out

        s4p_path = Cal_0504.generate_s4p(
            w_um=float(w_um),
            r=float(r),
            output_dir=S4P_DIR,
            # Important: use automatic W/R geometry here, not Cal_0504's
            # manual ADS-probing defaults.
            w_line_um=None,
            line_length_um=None,
            length_scale=Cal_0504.DEFAULT_LENGTH_SCALE,
            slot_ratio=Cal_0504.DEFAULT_SLOT_RATIO,
            freq_start_ghz=float(FREQ_GHZ[0]),
            freq_stop_ghz=float(FREQ_GHZ[-1]),
            freq_step_ghz=float(FREQ_GHZ[1] - FREQ_GHZ[0]),
            quiet=True,
            write_manifest=True,
        )
        inductors = predict_l_params(float(w_um), float(r))
        local_cfg = dict(tf0504.CFG)
        local_cfg.update(
            {
                "s4p_top": str(s4p_path),
                "s4p_bot": str(s4p_path),
                "f_start_ghz": float(FREQ_GHZ[0]),
                "f_stop_ghz": float(FREQ_GHZ[-1]),
                "f_step_ghz": float(FREQ_GHZ[1] - FREQ_GHZ[0]),
                "z0": 50.0,
                "L13_nH": float(inductors["L13_nH"]),
                "L24_nH": float(inductors["L24_nH"]),
                "L56_pH": float(inductors["L56_pH"]),
                "L56V_extra_pH": 0.0,
                "out_dir": str(S6P_DIR),
                "out_name": f"tf6_0504_W{safe_tag(w_um)}_R{safe_tag(r)}",
            }
        )
        ntw = tf0504.build_tf_6port(local_cfg)
        ntw = align_network(ntw, FREQ_HZ, name=f"tf6_0504_W{safe_tag(w_um)}_R{safe_tag(r)}")
        self.cache[key] = ntw.copy()
        out = ntw.copy()
        out.name = name
        return out

    def describe(self, w_um: float, r: float) -> dict[str, Any]:
        geom = calculate_compensation_msl(float(w_um), float(r))
        inductors = predict_l_params(float(w_um), float(r))
        return {
            "W_um": float(w_um),
            "R": float(r),
            "cal0504_s4p": str(self.s4p_path_for(w_um, r)),
            "geometry": geom,
            "inductors": inductors,
            "length_scale": Cal_0504.DEFAULT_LENGTH_SCALE,
            "slot_ratio": Cal_0504.DEFAULT_SLOT_RATIO,
            "model_note": "no compensation MLIN, no L56delta; Cal_0504 S4P generated in auto-W/R mode",
        }


def gain_metrics(ntw: rf.Network) -> tuple[np.ndarray, np.ndarray, dict[str, Any]]:
    gain = s21_db(ntw)
    s11 = 20.0 * np.log10(np.maximum(np.abs(ntw.s[:, 0, 0]), 1e-30))
    mask_35_75 = (FREQ_GHZ >= 35.0) & (FREQ_GHZ <= 75.0)
    gw = gain[mask_35_75]
    peak_30_80 = float(np.max(gain))
    threshold = peak_30_80 - 3.0
    min_35_75 = float(np.min(gw))
    max_35_75 = float(np.max(gw))
    min_30_80 = float(np.min(gain))
    max_30_80 = float(np.max(gain))
    metrics = {
        "s21_min_35_75_db": min_35_75,
        "s21_max_35_75_db": max_35_75,
        "s21_mean_35_75_db": float(np.mean(gw)),
        "s21_ripple_35_75_db": float(max_35_75 - min_35_75),
        "s21_peak_30_80_db": peak_30_80,
        "s21_min_30_80_db": min_30_80,
        "s21_max_30_80_db": max_30_80,
        "s21_ripple_30_80_db": float(max_30_80 - min_30_80),
        "threshold_3db_from_peak_30_80_db": threshold,
        "max_3db_deficit_35_75_db": float(max(threshold - min_35_75, 0.0)),
        "max_low_gain_violation_35_75_db": float(max(16.0 - min_35_75, 0.0)),
        "max_high_gain_violation_35_75_db": float(max(max_35_75 - 20.5, 0.0)),
        "strict_pass_35_75": bool(min_35_75 >= 16.0 and max_35_75 <= 20.5 and min_35_75 >= threshold),
        "ideal_pass_30_80": bool(min_30_80 >= 16.0 and max_30_80 <= 20.5 and min_30_80 >= threshold),
        "s11_min_30_80_db": float(np.min(s11)),
        "s11_max_30_80_db": float(np.max(s11)),
    }
    return gain, s11, metrics


def s_from_y_2port(y2: np.ndarray, z0: float = 50.0) -> np.ndarray:
    """Convert a stack of 2-port admittance matrices to S parameters."""
    eye = np.eye(2, dtype=complex)[None, :, :]
    return (eye - z0 * y2) @ np.linalg.inv(eye + z0 * y2)


def add_network_y(
    y_global: np.ndarray,
    y_local: np.ndarray,
    port_to_node: list[int | None],
) -> None:
    """Stamp a multiport Y matrix into the fixed PA nodal admittance matrix.

    ``None`` means that the corresponding circuit port is tied to ideal ground.
    Open ports are represented as ordinary internal nodes and later eliminated.
    """
    for local_i, node_i in enumerate(port_to_node):
        if node_i is None:
            continue
        for local_j, node_j in enumerate(port_to_node):
            if node_j is None:
                continue
            y_global[:, node_i, node_j] += y_local[:, local_i, local_j]


def build_full_pa_y_fast(
    *,
    driver_s4p: rf.Network,
    final_s4p: rf.Network,
    imn: rf.Network,
    ismn: rf.Network,
    omn: rf.Network,
) -> np.ndarray:
    """Return the exact 2-port PA Y matrix for the agreed fixed connections.

    This is algebraically equivalent to ``build_full_pa_network`` with
    ``include_dc_blocks=False`` for the 0504 synthesis topology, but avoids
    constructing a scikit-rf ``Circuit`` for every grid point.

    Node convention:
      0 PA input, 1 PA output,
      2/3 driver input pair, 4/5 driver output pair,
      6/7 final input pair, 8/9 final output pair,
      10/11/12 open MATAP nodes for IMN/ISMN/OMN.
    """
    nfreq = len(driver_s4p.f)
    y_global = np.zeros((nfreq, 13, 13), dtype=complex)

    add_network_y(y_global, imn.y, [2, 3, 0, None, None, 10])
    add_network_y(y_global, driver_s4p.y, [2, 3, 4, 5])
    add_network_y(y_global, ismn.y, [4, 5, 6, 7, None, 11])
    add_network_y(y_global, final_s4p.y, [6, 7, 8, 9])
    add_network_y(y_global, omn.y, [8, 9, 1, None, None, 12])

    keep = [0, 1]
    internal = list(range(2, 13))
    y_reduced = np.zeros((nfreq, 2, 2), dtype=complex)
    for idx in range(nfreq):
        ykk = y_global[idx][np.ix_(keep, keep)]
        yki = y_global[idx][np.ix_(keep, internal)]
        yik = y_global[idx][np.ix_(internal, keep)]
        yii = y_global[idx][np.ix_(internal, internal)]
        y_reduced[idx] = ykk - yki @ np.linalg.solve(yii, yik)
    return y_reduced


